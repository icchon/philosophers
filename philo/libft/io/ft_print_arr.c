#include "ft_io.h"

void	ft_print_arr(int *arr, int n)
{
	int	i;

	i = 0;
	while (i < n)
	{
		ft_printf("%d ", arr[i]);
		i++;
	}
	ft_printf("\n");
	return ;
}
